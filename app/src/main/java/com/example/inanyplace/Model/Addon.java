package com.example.inanyplace.Model;

public class Addon {
    private String name;
    private Long price;

    public Addon() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Long getPrice() {
        return price;
    }

    public void setPrice(Long price) {
        this.price = price;
    }
}
